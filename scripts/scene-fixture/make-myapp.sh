A small web service: a rotating refresh-token auth flow and an HTML dashboard.

- `auth.go` / `tokens.go` -- the `/auth/refresh` rotation + reuse detection.
- `server.go` -- the HTML home handler and the `/static/` asset handler.

Work in flight lives in the `auth-refresh` plan (see the Seamless briefing).
EOF

cd "$dir"
git init -q
git add -A
git -c user.name='myapp' -c user.email='dev@myapp.local' commit -qm 'myapp: initial scaffold'
echo "make-myapp: scaffolded $dir"
